func main() {
	generateFontSheet()
}

func generateFontSheet() {
	const glyphW, glyphH = 8, 8
	glyphs := "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"
	cols := len(glyphs)

	img := image.NewRGBA(image.Rect(0, 0, cols*glyphW, glyphH))

	white := color.RGBA{R: 255, G: 255, B: 255, A: 255}

	for i, ch := range glyphs {
		drawChar(img, i*glyphW, 0, byte(ch), white)
	}

	f, err := os.Create("font.png")
	if err != nil {
		panic(err)
	}
	defer f.Close()
	if err := png.Encode(f, img); err != nil {
		panic(err)
	}
}

