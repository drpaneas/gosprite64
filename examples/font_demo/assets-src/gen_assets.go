func drawChar(img *image.RGBA, ox, oy int, ch byte, c color.RGBA) {
	data, ok := font8x8[ch]
	if !ok {
		return
	}
	for row := 0; row < 8; row++ {
		b := data[row]
		for col := 0; col < 8; col++ {
			if b&(1<<col) != 0 {
				img.SetRGBA(ox+col, oy+row, c)
			}
		}
	}
}
